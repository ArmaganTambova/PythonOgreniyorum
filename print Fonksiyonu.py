# print() Fonksiyonu içerisinde matematiksel işlemler yapıp onları da yazdırabilirsiniz.

print("Benim yaşım: ", 25)

# print() Fonksiyonu içerisinde bu şekilde iki farklı türden veriyi birlikte kullanabilirsiniz.
# Sadece iki farklı türle sınırlı değil, print(string, int, double, int, float) şeklinde birbirinden farklı türleri virgül ile ayırarak istediğiniz şekilde kullanabilirsiniz.

print("Benim yaşım: " + str(25))

# Aynı zamanda farklı türden verileri print() içerisinde tür dönüşümü yaparak da kullanabilirisiz.
